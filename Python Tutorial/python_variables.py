
# variable is the name that is used to refer to the location, or we can say they are use to hold the value.

# variable can be both letter and digits but there are some rules to write variables:
# 1. Variables cannot contain special characters except for the underscore (_) symbol. 
# They should start with a letter or an underscore and can be followed by letters, digits, or underscores. 
# For example, valid variable names are my_variable, _num1, or text_2.

# 2. Variables cannot start with a number. They should begin with a letter or an underscore. 
# For instance, 1num is an invalid variable name, but _1num or num1 are acceptable.

# 3. Variables in Python are case-sensitive, which means that 
# number = 4
# Number = 4
# number and Number would be treated as separate variables.

# 4. In Python, it is recommended to use lowercase letters for variable names to adhere to the PEP 8 style guide. 
# This helps improve code readability and consistency. For example, number is preferred over Number.

# 5. Python is a dynamically-typed language, which means that you don't need to explicitly declare the type of a variable. 
# The type is inferred based on the value assigned to the variable.

# 6. You can assign different types of values to the same variable throughout the program. 
# For example, you can initially assign an integer value to a variable and later assign a string to the same variable.
# Example:
my_variable = 10  # Assigning an integer value
print(my_variable)  # Output: 10

my_variable = "Hello, World!"  # Reassigning a string value
print(my_variable)  # Output: Hello, World!

# 7. Python variable names should be descriptive and meaningful to enhance code readability. 
# For example, instead of text, you could use greeting_message or welcome_message.


