# 1. Numeric Literals:
# Integer literals: Whole numbers without a fractional part.
age = 25
count = -10
print("age: ", age)
print("count: ", count)

# Floating-point literals: Numbers with a fractional part.
pi = 3.14
height = -1.75
print("pi: ", pi)
print("height: ", height)

# Complex literals: Numbers with a real and imaginary part.
z = 3 + 4j
print("z: ", z)

# 2. String Literals:

# Enclosed in single quotes ('') or double quotes ("").
# They represent a sequence of characters.
name = "John Doe"
message = 'Hello, world!'
print("name: ", name)
print("message: ", message)

# 3. Boolean Literals:

# Two possible values: True and False.
is_raining = True
is_sunny = False
print("is_raining: ", is_raining)
print("is_sunny: ", is_sunny)
# 4. None Literal:

# Represents the absence of a value.
result = None
print("result: ", result)

# 5. List Literals:

# Enclosed in square brackets ([]).
# Ordered collection of items, separated by commas.
numbers = [1, 2, 3, 4, 5]
print("numbers: ", numbers)

# 6. Tuple Literals:

# Enclosed in parentheses ().
# Ordered, immutable collection of items, separated by commas.
coordinates = (3.5, 2.1)
print("coordinates: ", coordinates)

# 7. Dictionary Literals:

# Enclosed in curly braces ({}) and consists of key-value pairs separated by colons (:).
person = {"name": "Alice", "age": 30, "city": "London"}
print("person: ", person)

# 8. Set Literals:

# Enclosed in curly braces ({}) or you can use the set() function.
# Unordered collection of unique elements.
unique_numbers = {1, 2, 3, 4, 5}
print("unique_numbers: ", unique_numbers)
