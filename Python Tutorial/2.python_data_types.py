
# 1. Numeric Types:

# 1.1. Integer (int): Whole numbers without a fractional part.

num  = 25
print("value: ", num)
print("1.1. This is:", type(num), "type. \n")

# 1.2. Float (float): Numbers with a fractional part.
num = 1.23
print("value: ", num)
print("1.2. This is:", type(num), "type. \n")

# 1.3. Complex (complex): Numbers with a real and imaginary part.
num = 3 + 4j
print("value: ", num)
print("1.3. This is:", type(num), "type. \n")

# 2. Boolean Type:

# 2.1. Boolean (bool): Represents truth values, either True or False.
python_is_easy = True
print("value: ", python_is_easy)
print("2.1. This is:", type(python_is_easy), "type. \n")

# 3. Sequence Types:

# 3.1. String (str): A sequence of characters.
name = "Niraj Subedi"
print("value: ", name)
print("3.1. This is:", type(name), "type. \n")

# 3.2. List (list): An ordered collection of items.
numbers = [1, 2, 3, 4, 5]
print("value: ", numbers)
print("3.2. This is:", type(numbers), "type. \n")

# 3.2 Tuple (tuple): An ordered, immutable collection of items.
numbers = (3, 2.1)
print("value: ", numbers)
print("3.3. This is:", type(numbers), "type. \n")

# 4. Mapping Type:

# 4.1. Dictionary (dict): A collection of key-value pairs.
person = {"name": "niraj", "age": 22, "city": "Bharatpur"}
print("value: ", person)
print("4.1. This is:", type(person), "type. \n")

# 5. Set Types:

# 5.1. Set (set): An unordered collection of unique elements.
print("value: ", numbers)
numbers = {1, 2, 3, 4, 5}
print("5.1. This is:", type(numbers), "type. \n")

# 5.2 Frozen Set (frozenset): An immutable version of a set.
vowels = frozenset({'a', 'e', 'i', 'o', 'u'})
print("value: ", vowels)
print("5.2. This is:", type(vowels), "type. \n")

# 6. None Type:

# 6.1. None (None): Represents the absence of a value.
result = None
print("value: ", result)
print("6.1. This is:", type(result), "type. \n")

