
# 1. Numeric Types:

# 1.1. Integer (int): Whole numbers without a fractional part.

num  = 25 
print("1.1. type of num is:", type(num))

# 1.2. Float (float): Numbers with a fractional part.
num = 1.23
print("1.2. type of num is:", type(num))

# 1.3. Complex (complex): Numbers with a real and imaginary part.
num = 3 + 4j
print("1.3. type of num is:", type(num))

# 2. Boolean Type:

# 2.1. Boolean (bool): Represents truth values, either True or False.
python_is_easy = True
print("2.1. type of python_is_easy is:", type(python_is_easy))

# 3. Sequence Types:

# 3.1. String (str): A sequence of characters.
name = "Niraj Subedi"
print("3.1. type of name is:", type(name))

# 3.2. List (list): An ordered collection of items.
numbers = [1, 2, 3, 4, 5]
print("3.2. type of numbers is:", type(numbers))

# 3.2 Tuple (tuple): An ordered, immutable collection of items.
numbers = (3, 2.1)
print("3.3. type of numbers is:", type(numbers))

# 4. Mapping Type:

# 4.1. Dictionary (dict): A collection of key-value pairs.
person = {"name": "niraj", "age": 22, "city": "Bharatpur"}
print("4.1. type of person is:", type(person))

# 5. Set Types:

# 5.1. Set (set): An unordered collection of unique elements.
numbers = {1, 2, 3, 4, 5}
print("5.1. type of numbers is:", type(numbers))

# 5.2 Frozen Set (frozenset): An immutable version of a set.
vowels = frozenset({'a', 'e', 'i', 'o', 'u'})
print("5.2. type of vowels is:", type(vowels))

# 6. None Type:

# 6.1. None (None): Represents the absence of a value.
result = None
print("6.1. type of result is:", type(result))

